                    <p class="body">The founders of Quip believed the toothbrush &#8211; although an everyday object &#8211; had been generally overlooked. They saw consumers being consistently faced with the same array of colours & standard features when shopping for a new brush, regardless of price point, which had led to an indifference to both the toothbrush and its purpose. The founders who themselves were successful industrial designers therefore sought to revamp the toothbrush, and through a redesign of its aesthetics, functionality and branding turned it into a lifestyle product.</p><p class="body">Using premium design, materials and engineering the founders brought the toothbrush in line with other daily use products. It was designed to be modular with interchangeable handles, to enable consumers to further accessorise with colours, materials and finishes to match their modern lifestyle. The founders aimed to utilise this feature fully by galvanising a community of fellow designers to build a catalogue of 3D printed plastic handles, with around thirty different designs in six different colours available at launch, all in addition to giving particular consumers the option to create their own designs, to be uploaded, printed and delivered.</p><p class="body">De Charles worked with Quip to advise on, design and deliver the internal hardware for the toothbrush's optional electronic module. Quip needed this module to be robust and powerful enough control a toothbrush motor, be functionally able to handle capacitive touch in a noisy environment, brushing modes and gestures, and be small enough to work fit into the designers' sleek interior.</p><p class="body">The module De Charles eventually delivered had the additional capacity for the Quip team's planned expansions. Quip have gone on to raise VC funding and release their brush using a subscription model that has not yet been widely used in the dental hygiene market.</p>
