                    <p class="body">Solis was founded to build renewable products for the 1.3 billion people living off-grid in Africa and South Asia. There first product was one of the first solar lanterns to utilise USB charging, which enabled it to charge mobile phones and billions of other consumer devices on the market. After a stall in sales due chiefly to low income customers with limited access to credit, who also lost confidence due to an influx of cheap, poor quality lanterns, Solis worked with De Charles to change strategy.</p><p class="body">De Charles&#8217; market research led to it repositioning the company into an aspirational brand with products that transcend borders, cultures and markets. De Charles re-designed its initial product and created others in a range for additional scenarios complementary to that of off-grid, namely outdoors, domestic backup and on-the-go. The products were designed to be functionally, aesthetically and economically appropriate for them to transcend borders and build credibility.</p><p class="body">A market for backup power exists globally.  Grid connected peoples in less developed countries can suffer from acute blackouts and experience downtime due to load shedding. In more developed countries with pervasive power infrastructure, increasing energy prices, brownouts and blackouts are a growing experience stemming from burgeoning demand, and new and necessary efforts to transition aging networks to renewable ones of the future.</p><p class="body">The outdoor market covers activities such as camping, hiking, fishing, and cycling, and music festivals all of which &#8211; in the modern world &#8211; now require portable power and telecommunication products to be brought along for survival. The North Americans outdoor market was valued at USD646billion in 2011, while that of Europe was estimated at EUR10bn. Music festivals on the other hand are attended predominantly by loyal and digitally savvy millennials, most of whom would strongly align with the Solis renewable innovation concept. In the burgeoning US music festival scene, Coachella grossed approximately USD84million in ticket sales alone over two weekends in summer 2015, and since the turn of the century festivals have been fast becoming global.</p>
