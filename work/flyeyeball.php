                    <p class="body">A continuing trend amongst organisations with operatives who have the potential to work in hazardous, confined or otherwise hostile environments, is the delegation of as much work as possible to remote unmanned devices. However, no device has yet been commercialised that is suitable for close-range, ground-based surveillance.</p><p class="body">The current solutions require individuals to enter first into environments with very limited knowledge, meaning they are unable to pre-analyse potential hazards, threats, and instabilities. In the fire and rescue services for example, robotic exploratory devices have made only little ingress as a way to search for survivors of disastrous events. The 2008 Sichuan Earthquake in China caused the collapse of many buildings, and is an example where &#8211; without prior information &#8211; rescue services struggled to locate and free survivors trapped in the rubble. In situations like these, especially in frontier markets, people could be more promptly rescued if a small, robust, mobile, and inexpensive device could be easily launched into unstable settings, and provide real-time information for pre-analysis.</p><p class="body">Our concept solution for this was the FlyEye Ball, which we engineered to be small and elegantly cost-effective, and gather rich data in real-time in a wide range of surroundings with minimal operator input. The FlyEye Ball contains six cameras, positioned to record an almost entire spherical panorama, data routing and storage hardware, and a transmitter to relay the information back to a display device.</p><p class="body">With the bulk of data processing performed onboard, FlyEye Ball&#8217;s information can be displayed on even light hardware that can connect to its Wi-Fi network and run its application.</p>
