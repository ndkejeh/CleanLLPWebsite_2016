                    <p class="body">The International Energy Agency figures stated that in 2011 only 30% of the population in Sub-Saharan Africa had access to the electricity grid, with the remaining population in absolute darkness. Worrying still, large bands of those connected are in 'partial darkness' experiencing a heavily disrupted, sporadic service, meaning the percentage grid-connected with an effective and reliable supply is much lower.</p><p class="body">Africa&#8217;s power capacity is insufficient, evinced by sub-Saharan Africa&#8217;s 49 countries collectively having approximately four-fifths of the UK&#8217;s capacity. The fact that most of the continent&#8217;s power stations were commissioned before 1990 illustrates the shallow levels of investment the sector has historically received, and that less than 5% of the total of global power project deals to have taken place between 1990 and 2010 were in Africa, shows that capacity growth is not rising fast enough. Therefore, the continent unsurprisingly has the lowest power capacity density, which &#8211; alongside poor power management &#8211; engenders blackouts. Electricity is unavailable in Nigeria for on average eight months of the year, and the severity and frequency of these blackouts, particularly in West Africa, has affected the cost and ease of carrying out business.</p><p class="body">Some of the De Charles team were founder members of e.quinox, and within it developed a sustainable financial model and engineered a solar PV product nicknamed the energy kiosk, which was successfully pitched to the Rwandan Ministry of Infrastructure and installed in Minazi, Rwanda in 2009. The product brought renewable power to sixty homes in a remote off-grid region, using a discrete and modular model that mimicked the community&#8217;s existing use of kerosene.</p><p class="body">This first project was able to create jobs for a kiosk manager and maintenance staff, the salaries of whom were completely financed by energy kiosk sales to the community. The founders setup e.quinox to be an ongoing student-led initiative and NGO, and with additional projects run by subsequent students, power had been provided to over 2,000 homes as of 2014.</p><p class="body">The e.quinox team went on to win prizes for innovation that included six-figure sums and an eponymous planet, from prestigious entities such as the Institute of Electrical and Electronic Engineers, JP Morgan, GE Ecomagination, and the Lincoln Laboratory in the Massachusetts Institute of Technology.</p>
